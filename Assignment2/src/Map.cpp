Map::Map(){
  this->grass = TextureManager::loadTexture(GRASS_TILE_FILE_PATH);
  this->flip = SDL_FLIP_NONE;

  this->loadMap(defaultMap);

  this->srcRect.x = 0;
  this->srcRect.y = 0;
  this->srcRect.h = GRASS_TILE_WIDTH_HEIGHT;
  this->srcRect.w = GRASS_TILE_WIDTH_HEIGHT;

  this->desRect.h = GRASS_TILE_WIDTH_HEIGHT_2_DRAW;
  this->desRect.w = GRASS_TILE_WIDTH_HEIGHT_2_DRAW;
}

Map::~Map(){
  SDL_DestroyTexture(grass);
  // SDL_DeleteTexture(line);
}

void Map::loadMap(int arr[MAP_HEIGHT][MAP_WIDTH]){
  for(int i = 0; i < MAP_HEIGHT; i+=1){
    for(int j = 0; j < MAP_WIDTH; j+=1){
      this->map[i][j] = arr[i][j];
    }
  }
}

void Map::drawMap(){
  int type;
  for(int i = 0; i < MAP_HEIGHT; i+=1){
    for(int j = 0; j < MAP_WIDTH; j+=1){
      type = map[i][j];
      this->desRect.x = GRASS_TILE_WIDTH_HEIGHT_2_DRAW*j;
      this->desRect.y = GRASS_TILE_WIDTH_HEIGHT_2_DRAW*i;

      switch(type){
        case 0:
          TextureManager::draw(grass, this->srcRect, this->desRect, this->flip);
          break;

        default:
          break;
      }

    }
  }
}